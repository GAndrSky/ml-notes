$ErrorActionPreference = "Stop"

function ConvertTo-HtmlText {
  param([string]$Text)
  return [System.Net.WebUtility]::HtmlEncode($Text)
}

function New-ParagraphHtml {
  param([string[]]$Paragraphs)
  $parts = New-Object System.Collections.Generic.List[string]
  foreach ($paragraph in $Paragraphs) {
    [void]$parts.Add("      <p>$(ConvertTo-HtmlText $paragraph)</p>")
  }
  return $parts -join "`r`n"
}

function New-ListHtml {
  param([string[]]$Items)
  $parts = New-Object System.Collections.Generic.List[string]
  foreach ($item in $Items) {
    [void]$parts.Add("        <li>$(ConvertTo-HtmlText $item)</li>")
  }
  return ($parts -join "`r`n")
}

function New-TheoryBlock {
  param([hashtable]$Note, [string]$Kind)
  $titleKey = if ($Kind -eq "mechanism") { "MechanismTitle" } else { "FailureTitle" }
  $bodyKey = if ($Kind -eq "mechanism") { "Mechanism" } else { "Failure" }
  $kicker = if ($Kind -eq "mechanism") { "Теория по месту" } else { "Как не ошибиться" }
  $title = ConvertTo-HtmlText $Note[$titleKey]
  $body = New-ParagraphHtml $Note[$bodyKey]
  return @"

    <aside class="classic-theory-note" data-classic-theory="$Kind">
      <div class="classic-theory-note__kicker">$kicker</div>
      <h3>$title</h3>
$body
    </aside>
"@
}

function New-VizBlock {
  param([hashtable]$Note)
  if (-not $Note.ContainsKey("Viz")) { return "" }
  $title = ConvertTo-HtmlText $Note["VizTitle"]
  $items = New-ListHtml $Note["Viz"]
  return @"

    <div class="classic-viz-note" data-classic-viz-note="1">
      <div class="classic-viz-note__kicker">Как читать визуализацию</div>
      <h3>$title</h3>
      <ul>
$items
      </ul>
    </div>
"@
}

function Remove-ClassicInlineBlocks {
  param([string]$Html)
  $clean = [regex]::Replace($Html, '(?is)\s*<aside\b[^>]*data-classic-theory="(?:mechanism|failure)"[^>]*>.*?</aside>\s*', "`r`n")
  $clean = [regex]::Replace($clean, '(?is)\s*<div\b[^>]*data-classic-viz-note="1"[^>]*>.*?</div>\s*', "`r`n")
  return $clean
}

function Get-DivBlockEndPositions {
  param([string]$Html, [string]$Marker)
  $positions = New-Object System.Collections.Generic.List[int]
  $pos = 0
  $tagRegex = [regex]::new('(?is)<div\b[^>]*>|</div>')

  while ($true) {
    $start = $Html.IndexOf($Marker, $pos, [System.StringComparison]::OrdinalIgnoreCase)
    if ($start -lt 0) { break }
    $matches = $tagRegex.Matches($Html, $start)
    $depth = 0
    $end = -1
    foreach ($tag in $matches) {
      if ($tag.Value -match '^<div\b') { $depth += 1 } else { $depth -= 1 }
      if ($depth -eq 0) {
        $end = $tag.Index + $tag.Length
        break
      }
    }
    if ($end -gt $start) {
      [void]$positions.Add($end)
      $pos = $end
    } else {
      $pos = $start + $Marker.Length
    }
  }

  return $positions
}

function Get-FirstCanvasEnd {
  param([string]$Html)
  $scriptIndex = $Html.IndexOf("<script", [System.StringComparison]::OrdinalIgnoreCase)
  $limit = if ($scriptIndex -gt 0) { $scriptIndex } else { $Html.Length }
  $prefix = $Html.Substring(0, $limit)
  $match = [regex]::Match($prefix, '(?is)<canvas\b[^>]*>.*?</canvas>')
  if ($match.Success) { return $match.Index + $match.Length }
  return -1
}

function Add-Insertion {
  param([hashtable]$Insertions, [int]$Position, [string]$Text)
  if ($Position -lt 0 -or [string]::IsNullOrWhiteSpace($Text)) { return }
  if ($Insertions.ContainsKey($Position)) {
    $Insertions[$Position] = $Insertions[$Position] + $Text
  } else {
    $Insertions[$Position] = $Text
  }
}

