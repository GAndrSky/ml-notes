$root = Split-Path -Parent $PSScriptRoot
$classicDir = Join-Path $root "02_classic_ml"
$files = Get-ChildItem -Path $classicDir -Filter *.html
$marker = '<div class="formula-anatomy" data-static-formula-anatomy="1">'
$updated = 0

foreach ($file in $files) {
  if (-not $notes.ContainsKey($file.Name)) { continue }

  $html = Get-Content -LiteralPath $file.FullName -Raw -Encoding UTF8
  $html = Remove-ClassicInlineBlocks $html

  $note = $notes[$file.Name]
  $formulaEnds = @(Get-DivBlockEndPositions $html $marker)
  if ($formulaEnds.Count -eq 0) { continue }

  $insertions = @{}
  Add-Insertion $insertions $formulaEnds[0] (New-TheoryBlock $note "mechanism")

  $failureIndex = if ($formulaEnds.Count -gt 2) { [Math]::Floor($formulaEnds.Count / 2) } else { $formulaEnds.Count - 1 }
  Add-Insertion $insertions $formulaEnds[$failureIndex] (New-TheoryBlock $note "failure")

  $canvasEnd = Get-FirstCanvasEnd $html
  if ($canvasEnd -gt 0) {
    Add-Insertion $insertions $canvasEnd (New-VizBlock $note)
  }

  foreach ($position in ($insertions.Keys | Sort-Object -Descending)) {
    $html = $html.Insert([int]$position, $insertions[$position])
  }

  Set-Content -LiteralPath $file.FullName -Value $html -Encoding UTF8
  Write-Host "$($file.Name): inline theory inserted"
  $updated += 1
}

Write-Host "Updated classic ML files: $updated"

